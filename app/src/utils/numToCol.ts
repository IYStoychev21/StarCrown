
export function convertNumberToCol(num: number) {
    if (num === 1) {
        return 'grid-cols-1'
    } else if (num === 2) {
        return 'grid-cols-2'
    } else if (num === 3) {
        return 'grid-cols-3'
    } else if (num === 4) {
        return 'grid-cols-4'
    } else if (num === 5) {
        return 'grid-cols-5'
    } else if (num === 6) {
        return 'grid-cols-6'
    } else if (num === 7) {
        return 'grid-cols-7'
    } else if (num === 8) {
        return 'grid-cols-8'
    } else if (num === 9) {
        return 'grid-cols-9'
    } else if (num === 10) {
        return 'grid-cols-10'
    }
}